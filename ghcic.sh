#'('\										# —Å–ø–µ—Ä–≤–∞ –ª–æ–≤–∏–º —Ç–æ, —á—Ç–æ: –ª–∏–±–æ —É–∂–µ –ø–æ–π–º–∞–ª–∏, –ª–∏–±–æ –Ω–µ –ø–æ–¥–ª–µ–∂–∏—Ç –æ—Ç–ª–æ–≤—É:
#'('\
#'"((\\.)|[^\\"])*"|'\						# –°—Ç—Ä–æ–∫–∏ –≤–∏–¥–∞ "—ç—Ç–æ\"—Å—Ç—Ä–æ–∫–∞"
#'\t_*[[:alnum:]]+[[:alnum:].]*\t|'\		# –∏–¥–µ–Ω—Ç–∏—Ñ–∏–∫–∞—Ç–æ—Ä—ã, –∏–º–µ–Ω–∞ —Ñ–∞–π–ª–æ–≤ ___file.txt
#'\t-?[[:digit:]]+([.][[:digit:]]+)?\t|'\	# —á–∏—Å–ª–∞, –≤–∫–ª—é—á–∞—è –¥—Ä–æ–±–Ω—ã–µ –∏/–∏–ª–∏ –æ—Ç—Ä–∏—Ü–∞—Ç–µ–ª—å–Ω—ã–µ
#'\t[=*}{()[]\t|'\							# —Å–∏–º–≤–æ–ª—ã =*}{()[
#'\t[]]\t|'\								# —Å–∏–º–≤–æ–ª ]
#'[, ]|'\									# –∑–∞–ø—è—Ç—É—é –∏ –ø—Ä–æ–±–µ–ª –Ω–∞ —Ä–∞—Å–∫—Ä–∞—à–∏–≤–∞–µ–º.
#'\t['\										# —Å–∏–º–≤–æ–ª –≤ –æ–ø–æ—Å—Ç—Ä–æ—Ñ–∞—Ö: —á–∏—Å–ª–æ –∏–ª–∏ –±—É–∫–≤–∞ '[[:alnum:]]'
#"'][[:alnum:]]['"\							#
